public class ConcatenaCaracteres{
    public static void main(String[] args) {
        char letra1 = 'a';
        char letra2 = 'b';
         System.out.println(letra1);
        System.out.println(letra2);
        // En esta linea los char son tomados como int cogiendo como valor el codigo ASCII correspondiente
        System.out.println(letra1 + letra2); 
         System.out.println(letra1 + "" + letra2);

    }

}